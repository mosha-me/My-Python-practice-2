""" পাইথনে **Escape Sequence** হলো এমন কিছু বিশেষ ক্যারেক্টার যা ব্যাকস্ল্যাশ (**`\`**) দিয়ে শুরু হয় এবং টেক্সটের ভেতরে কোনো বিশেষ কাজ সম্পন্ন করার জন্য ব্যবহৃত হয় [৩৮]। যখন পাইথন ইন্টারপ্রেটার কোনো টেক্সটের মধ্যে ব্যাকস্ল্যাশ দেখে, সে বুঝতে পারে যে সাধারণ সিকোয়েন্সটি ভেঙে এখন একটি বিশেষ নির্দেশ পালন করতে হবে [৩৯, ৪৪]। """

# সোর্স অনুযায়ী পাইথনে বহুল ব্যবহৃত কিছু এস্কেপ সিকোয়েন্স নিচে দেওয়া হলো:
"""
*   **`\n` (New Line):** এটি আউটপুটে একটি **নতুন লাইন** তৈরি করতে ব্যবহৃত হয় [৩৯, ৪৬]।
*   **`\t` (Tab):** এটি শব্দের মাঝে একটি **ট্যাব স্পেস** বা বড় ফাঁকা জায়গা তৈরি করে [৩৯, ৪৮]।
*   **`\"` (Double Quote):** যদি আপনি ডবল কোটেশনের ভেতরে থাকা কোনো টেক্সটের মাঝে ডবল কোট দেখাতে চান, তবে এটি ব্যবহার করতে হয় [৩৯, ৪১]। এটি পাইথনকে বলে যে এই কোটেশনটি টেক্সটের অংশ, স্ট্রিংয়ের শেষ নয় [৪১]।
*   **`\'` (Single Quote):** একইভাবে টেক্সটের ভেতরে **সিঙ্গেল কোট** প্রদর্শনের জন্য এটি ব্যবহৃত হয় [৩৯, ৪৩]।
*   **`\\` (Backslash):** আউটপুটে যদি একটি **আসল ব্যাকস্ল্যাশ** দেখাতে হয়, তবে দুটি ব্যাকস্ল্যাশ একসাথে লিখতে হয় [৩৯, ৪৪]।
*   **`\b` (Backspace):** এটি **ব্যাকস্পেস** হিসেবে কাজ করে [৪০]।
"""

# **কেন এটি প্রয়োজন?**
""" ধরা যাক, আপনি আউটপুটে `"Python"` শব্দটি ডবল কোটের ভেতর দেখাতে চান। আপনি যদি সরাসরি `print("Hi "Python"")` লেখেন, তবে পাইথন ভুল (Syntax Error) ধরবে কারণ সে মনে করবে প্রথম ডবল কোটেই মেসেজটি শেষ হয়ে গেছে [৪০]। এই সমস্যা সমাধানের জন্য এস্কেপ ক্যারেক্টার ব্যবহার করে লিখতে হয়: `print("Hi \"Python\"")` [৪১, ৪২]। এতে পাইথন বুঝতে পারে যে ভেতরের কোটগুলো মেসেজেরই অংশ [৪১]। """


