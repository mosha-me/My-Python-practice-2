""" পাইথনে **ফাংশন (Function)** হলো কোডের একটি ছোট এবং পুনরায় ব্যবহারযোগ্য অংশ যা একটি নির্দিষ্ট কাজ সম্পন্ন করার জন্য তৈরি করা হয় [৬৮৯]। সহজ কথায়, এটি আপনার কোডের ভেতরে থাকা একটি **"মিনি মেশিন" (Mini machine)** এর মতো [৬, ৩৪, ৬৮৯]। """

# ফাংশন সম্পর্কে সোর্স থেকে প্রাপ্ত মূল বিষয়গুলো নিচে আলোচনা করা হলো:
"""
*   **সহজ উদাহরণ:** একটি কফি মেশিনের কথা ভাবুন; আপনি এতে কফি বিন এবং পানি (ইনপুট) দেন, মেশিনটি ভেতরে কাজ করে এবং আপনাকে কফি (আউটপুট) দেয় [৩৪]। ফাংশনও ঠিক একইভাবে কাজ করে যেখানে আপনি তাকে কিছু তথ্য দেন এবং সে আপনাকে একটি ফলাফল প্রদান করে [৩৪]।
*   **ফাংশনের প্রধান দুটি অংশ:**
    ১. **ফাংশন ডেফিনিশন (Definition):** এখানে `def` কিওয়ার্ড ব্যবহার করে ফাংশনটি কী কাজ করবে তা লিখে রাখা হয় [৬৯৮]।
    ২. **ফাংশন কল (Call):** এটি হলো ফাংশনটিকে কার্যকর করার প্রক্রিয়া [৬৯৮, ৬৯৯]। যতক্ষণ না আপনি ফাংশনটিকে কল করছেন, ততক্ষণ এটি কোনো কাজ করবে না [৬৯৯, ৭০০]।
*   **ইনপুট ও আউটপুট:** ফাংশনের ভেতরে যে ডেটা পাঠানো হয় তাকে বলা হয় **প্যারামিটার বা আর্গুমেন্ট (Parameters/Arguments)** [৭১০, ৭১৪]। আর কাজ শেষে ফাংশন যে ফলাফলটি আমাদের ফেরত দেয় তাকে বলা হয় **রিটার্ন ভ্যালু (Return value)** [৭১০, ৭৫২]।
*   **ফাংশনের ধরণ:** পাইথনে ফাংশন প্রধানত তিন ধরণের উৎস থেকে আসে:
    *   **Built-in functions:** এগুলো পাইথনের সাথেই থাকে, যেমন- `print()`, `len()`, `type()` [৩৫, ৬৯৩, ৬৯৪]।
    *   **Library functions:** এগুলো স্ট্যান্ডার্ড বা এক্সটারনাল লাইব্রেরি থেকে ইম্পোর্ট করে নিতে হয়, যেমন- `math` মডিউলের ফাংশনগুলো [৬৯৪, ৬৯৫]।
    *   **User-defined functions:** এগুলো ডেভেলপাররা নিজেদের প্রয়োজন অনুযায়ী স্ক্র্যাচ থেকে তৈরি করেন [৩৫, ৬৯৬, ৬৯৭]।
*   **কেন ব্যবহার করবেন:** 
    *   একই লজিক বারবার না লিখে একবার লিখে তা **পুনরায় ব্যবহার (Reuse)** করার জন্য [৬৯০, ৬৯১]।
    *   জটিল সমস্যাগুলোকে ছোট ছোট এবং সহজ অংশে ভাগ করার জন্য (Modularity) [৬৯২]।
    *   কোডকে আরও পেশাদার, পরিষ্কার এবং পড়ার যোগ্য করে তোলার জন্য [৬৯১]।
"""

# সংক্ষেপে, ফাংশন ব্যবহারের মূল উদ্দেশ্য হলো **"Don't Reinvent the Wheel"** অর্থাৎ যে কাজ একবার করা হয়েছে তা বারবার না করে বুদ্ধিমত্তার সাথে পুনরায় ব্যবহার করা [৬৯৬]।


# ------------------------------------------------------
